#include "libft.h"

char		*ft_strrchr(const char *s, int c)
{
	int i;

	c = (char)c;
	i = 0;
	while (*s != 0)
	{
		i++;
		s++;
	}
	while (i > 0)
	{
		if (*s == c)
			break ;
		i--;
		s--;
	}
	return (*s == c ? ((char *)s) : NULL);
}
